interface SeedFamilyHome {
  //* General Info
  zone_house: string;
  name_home?: string;

  //* Info Home address
  province?: string;
  district: string;
  address: string;
  is_active?: boolean;

  //* Relations
  their_preacher: string;
}
interface SeedOffering {
  //* General Info
  type: string;
  sub_type?: string;
  amount: number;
  currency: string;
  comments?: string;
  url_file?: string;

  //* Relations
  member_id?: string;
  family_home_id?: string;
  copastor_id?: string;
}

interface SeedUser {
  email: string;
  first_name: string;
  last_name: string;
  password: string;
  roles: string[];
}

interface SeedDataUser {
  users: SeedUser[];
}
interface SeedDataMember {
  members: SeedMember[];
}

interface SeedDataHouses {
  houses: SeedFamilyHome[];
}
interface SeedDataOffering {
  offerings: SeedOffering[];
}

