import {
  Get,
  Body,
  Patch,
  Param,
  Query,
  Controller,
  ParseUUIDPipe,
} from '@nestjs/common';
import {
  ApiTags,
  ApiParam,
  ApiBearerAuth,
  ApiOkResponse,
  ApiNotFoundResponse,
  ApiForbiddenResponse,
  ApiBadRequestResponse,
  ApiUnauthorizedResponse,
  ApiInternalServerErrorResponse,
} from '@nestjs/swagger';
import { SkipThrottle } from '@nestjs/throttler';

import { PaginationDto } from '@/common/dtos/pagination.dto';

import { Auth } from '@/modules/auth/decorators/auth.decorator';
import { GetUser } from '@/modules/auth/decorators/get-user.decorator';

import { User } from '@/modules/user/entities/user.entity';
import { UserRole } from '@/modules/auth/enums/user-role.enum';

import { UpdateExternalDonorDto } from '@/modules/external-donor/dto/update-external-donor.dto';

import { ExternalDonor } from '@/modules/external-donor/entities/external-donor.entity';
import { ExternalDonorService } from '@/modules/external-donor/external-donor.service';

@ApiTags('External Donors')
@ApiBearerAuth()
@ApiUnauthorizedResponse({
  description:
    'üîí Unauthorized: Missing or invalid Bearer Token. Please provide a valid token to access this resource.',
})
@ApiInternalServerErrorResponse({
  description:
    'üö® Internal Server Error: An unexpected error occurred on the server. Please check the server logs for more details.',
})
@ApiBadRequestResponse({
  description:
    '‚ùå Bad Request: The request contains invalid data or parameters. Please verify the input and try again.',
})
@ApiForbiddenResponse({
  description:
    'üö´ Forbidden: You do not have the necessary permissions to access this resource.',
})
@SkipThrottle()
@Controller('external-donor')
export class ExternalDonorController {
  constructor(private readonly externalDonorService: ExternalDonorService) {}

  //* FIND ALL
  @Get()
  @Auth()
  @ApiOkResponse({
    description:
      '‚úÖ Successfully completed: The operation was completed successfully and the response contains the requested data.',
  })
  @ApiNotFoundResponse({
    description:
      '‚ùì Not Found: The requested resource was not found. Please verify the provided parameters or URL.',
  })
  findAll(@Query() paginationDto: PaginationDto): Promise<ExternalDonor[]> {
    return this.externalDonorService.findAll(paginationDto);
  }

  //* UPDATE
  @Patch(':id')
  @Auth(UserRole.SuperUser, UserRole.AdminUser)
  @ApiOkResponse({
    description:
      '‚úÖ Successfully completed: The resource was successfully updated. The updated data is returned in the response.',
  })
  @ApiNotFoundResponse({
    description:
      '‚ùì Not Found: The requested resource was not found. Please verify the provided parameters or URL.',
  })
  @ApiParam({
    name: 'id',
    description:
      'Unique identifier of the external donor to be updated. This ID is used to find the existing record to apply the update.',
    example: 'f47c7d13-9d6a-4d9e-bd1e-2cb4b64c0a27',
  })
  update(
    @Param('id', ParseUUIDPipe) id: string,
    @Body() updateExternalDonorDto: UpdateExternalDonorDto,
    @GetUser() user: User,
  ): Promise<ExternalDonor> {
    return this.externalDonorService.update(id, updateExternalDonorDto, user);
  }
}
